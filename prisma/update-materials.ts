async function main() {
  const materials = await prisma.learningMaterial.findMany({
    where: { title: { in: Object.keys(CONTENT) } },
  })

  console.log(`找到 ${materials.length} 条资料，开始更新内容…\n`)

  for (const m of materials) {
    const text = CONTENT[m.title]
    if (!text) { console.log(`  ⚠️  无匹配内容：${m.title}`); continue }

    await prisma.learningMaterial.update({
      where: { id: m.id },
      data: {
        contentType: 'text',
        contentText: text,
        contentUrl: null,
        minReadSeconds: 60,   // 演示用：阅读 60 秒后即可标记完成
      },
    })
    console.log(`  ✅ 已更新：${m.title}`)
  }

  console.log('\n🎉 完成！所有资料已转为图文类型，复习时在平台内正常显示。')
}

main()
  .catch(e => { console.error('❌ 出错：', e); process.exit(1) })
  .finally(() => prisma.$disconnect())
